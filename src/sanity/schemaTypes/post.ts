import { defineField, defineType } from 'sanity'
import type { SanityDocument, Slug, Image } from 'sanity'
import type { Author } from './author'
import type { Category } from './category'
import type { BlockContent } from './blockContent'

/**
 * Interface for Post document type
 */
export interface Post extends SanityDocument {
	_type: 'post'
	title: string
	slug: Slug
	author: {
		_type: 'reference'
		_ref: string // Reference to an Author document
	}
	mainImage: Image & {
		alt?: string
	}
	categories: Array<{
		_type: 'reference'
		_ref: string // Reference to a Category document
	}>
	publishedAt: string // ISO date string
	body: BlockContent
}

export const postType = defineType({
	name: 'post',
	type: 'document',
	fields: [
		defineField({
			name: 'title',
			type: 'string'
		}),
		defineField({
			name: 'slug',
			type: 'slug',
			options: {
				source: 'title',
				maxLength: 96
			}
		}),
		defineField({
			name: 'author',
			type: 'reference',
			to: { type: 'author' }
		}),
		defineField({
			name: 'mainImage',
			type: 'image',
			options: {
				hotspot: true
			},
			fields: [
				{
					name: 'alt',
					type: 'string',
					title: 'Alternative Text'
				}
			]
		}),
		defineField({
			name: 'categories',
			type: 'array',
			of: [{ type: 'reference', to: { type: 'category' } }]
		}),
		defineField({
			name: 'publishedAt',
			type: 'datetime'
		}),
		defineField({
			name: 'body',
			type: 'blockContent'
		})
	],

	preview: {
		select: {
			title: 'title',
			author: 'author.name',
			media: 'mainImage'
		},
		prepare(selection) {
			const { author } = selection
			return { ...selection, subtitle: author && `by ${author}` }
		}
	}
})
